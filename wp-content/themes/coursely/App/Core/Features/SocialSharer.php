    public static function get_social_sharing_buttons($obj): array
	{
        if ($obj instanceof \WP_Post) {

            $postUrl = urlencode(get_permalink());
            $postTitle = htmlspecialchars(urlencode(html_entity_decode(get_the_title(), ENT_COMPAT, 'UTF-8')), ENT_COMPAT, 'UTF-8');
            $postThumbnail = wp_get_attachment_image_src(get_post_thumbnail_id($obj->ID), 'full') ? wp_get_attachment_image_src(get_post_thumbnail_id($obj->ID), 'full') : array(0);

            $facebookURL = 'https://www.facebook.com/sharer/sharer.php?u=' . $postUrl;
            $whatsappURL = 'https://api.whatsapp.com/send?text=' . $postTitle . ' ' . $postUrl;
            $pinterestURL = 'https://pinterest.com/pin/create/button/?url=' . $postUrl . '&amp;media=' .$postThumbnail[0] . '&amp;description=' . $postTitle;
            $linkedinURL = 'https://www.linkedin.com/sharing/share-offsite/?url=' . $postUrl;
			$telegramURL = 'https://t.me/share/url?url=' . $postUrl . '&text=' . $postTitle;
